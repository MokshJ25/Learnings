/* SYNTAX:
     while(conditions)
     {
        C++ STATEMENTS;
     }*/

     #include<iostream>
using namespace std;
int main()
{
    int i=1;
    while(i<=40)
    {
        cout<<i<<endl;
        i++;
    }
    return 0;

}

